//arrays

const fruits = ["Apple", "Mango", "Orange"];

console.log(fruits);
console.log(fruits.length);
console.log(fruits[fruits.length-1]);

//push
fruits.push("Banana");
console.log(fruits);

//pop
fruits.pop();
console.log(fruits);

//unshift
fruits.unshift("Banana");
console.log(fruits);

//unshift
fruits.shift();
console.log(fruits);
